// Helper functions
export const getYearlyTrends = () => {
  return placementData.map(year => ({
    year: year.year,
    placementRate: year.totals.placementPercent,
    totalplaced: year.totals.placed,
    highestPackage: year.totals.highestPackage,
    averagePackage: year.totals.averagePackage,
    eligibleStudents: year.totals.eligibleStudents,
  }));
};

export const getDecadeStats = () => {
  const totalplaced = placementData.reduce((sum, year) => sum + year.totals.placed, 0);
  const totalEligible = placementData.reduce((sum, year) => sum + year.totals.eligibleStudents, 0);
  const avgPlacement = placementData.reduce((sum, year) => sum + year.totals.placementPercent, 0) / placementData.length;
  const maxPackageEver = Math.max(...placementData.map(year => year.totals.highestPackage));
  const avgPackage = placementData.reduce((sum, year) => sum + year.totals.averagePackage, 0) / placementData.length;
  
  return { totalplaced, totalEligible, avgPlacement, maxPackageEver, avgPackage };
};

export const getTopPerformers = () => {
  const allCourses = placementData.flatMap(year => 
    year.courses.map(course => ({ ...course, year: year.year }))
  );
  
  return {
    topByPlacement: [...allCourses].sort((a, b) => b.placementPercent - a.placementPercent).slice(0, 5),
    topByPackage: [...allCourses].sort((a, b) => b.highestPackage - a.highestPackage).slice(0, 5),
    topByAverage: [...allCourses].sort((a, b) => b.averagePackage - a.averagePackage).slice(0, 5),
  };
};

